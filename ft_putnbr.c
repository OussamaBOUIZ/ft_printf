#include "libftprintf.h"

void	ft_putnbr(int nb)
{	
	long int	b;

	b = nb;
	if (b < 0 )
	{
		ft_putchar('-');
		b = b * (-1);
	}
	if (b >= 10 )
	{
		ft_putnbr(b / 10);
	}
	ft_putchar(b % 10 + '0');
}
